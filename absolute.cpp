/*
Complete this program so that when the input x is positive, it prints x, and when it is negative, it prints -x. 
Hint: use else.
*/

#include <iostream>
using namespace std;

int main() {
   int x;
   cin >> x;
   // print out either x or negative x
    if(x>=0)
        cout << x << endl;
    else
        cout << x << endl;






   return 0;
}
